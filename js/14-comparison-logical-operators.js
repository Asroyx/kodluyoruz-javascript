//********** Comparison Operators and Logical Operators *****
// Reference: https://www.w3schools.com/js/js_comparisons.asp

let price = "100";

// == Equal to (value only)
console.log("price == 1:", price == 1);
console.log("price == 100:", price == 100);

// === Equal to (value and type)
console.log("price === 1:", price === 1);
console.log("price === 100:", price === 100);
